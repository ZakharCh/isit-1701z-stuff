#include <iostream>

int main()
{
    // Объявление переменной
    // тип имя;
    //
    //  Целочисленные типы:
    //  со знаком
    //  char, short, int, long int (long), long long
    //  без знака
    //  unsigned char, unsigned short, unsigned int (unsigned),
    //  unsigned long int (unsigned long), unsigned long long
    //
    //  Вещественные типы (с плавающей точкой):
    //  float, double, long double
    //
    //  Булевый тип
    //  bool    -> true, false
    //

    int a = 5;
    int b;
    b = 23;

    std::cout << "a = " << a << std::endl;
    std::cout << "b = " << b << std::endl;

	return 0;
}

